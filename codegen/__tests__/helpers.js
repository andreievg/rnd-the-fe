/**
 * Test helpers for codegen/plugin.js.
 *
 * The whole point of these tests is to be READABLE: each test is a small
 * GraphQL schema + a query going IN, and the exact TypeScript the plugin emits
 * coming OUT. These helpers just wire the plugin up so a test reads like:
 *
 *   const out = generate({ schema, document });
 *   assert.strictEqual(resultType(out, "Q"), `{ ... expected ... }`);
 *
 * Nothing here mocks the plugin — `generate` calls the real plugin (the same
 * function codegen/run.js calls) so what the tests assert is exactly what ends
 * up in the *.generated.ts files.
 *
 * NOTE on names: the plugin PascalCases operation/fragment names for the emitted
 * TS identifiers (operation `stocktakeLines` → `StocktakeLinesResult`,
 * `StocktakeLines`, etc.). So the `name` you pass to these helpers is the
 * PascalCased name, e.g. resultType(out, "StocktakeLines").
 */
const { buildSchema, parse } = require("graphql");
const ts = require("typescript");
const plugin = require("../plugin.js");

const HEADER =
  "/* eslint-disable */\n" +
  "// THIS FILE IS GENERATED by codegen/plugin.js — do not edit by hand.\n";

/**
 * Run the plugin against an in-memory schema (SDL string) and one or more
 * GraphQL documents (query/fragment text).
 *
 * @param {object} opts
 * @param {string} opts.schema   GraphQL SDL.
 * @param {string|string[]} opts.document  Operation/fragment text. Multiple
 *        strings are treated as separate documents (matches how run.js feeds
 *        one file at a time, but lets a test split schema-ish chunks).
 * @returns {string} The full generated TypeScript file contents.
 */
function generate({ schema, document }) {
  const builtSchema = buildSchema(schema);
  const docs = (Array.isArray(document) ? document : [document]).map((text) => ({
    document: parse(text),
  }));
  return plugin.plugin(builtSchema, docs);
}

/**
 * Pull out the body of `export type <Name>Result = <BODY>;` for a given
 * operation name (or the first/only Result if no name is given). Trailing `;`
 * is dropped so tests can compare against a clean object-type literal.
 */
function resultType(generated, name) {
  return extractType(generated, "Result", name);
}

/** Same as resultType but for `export type <Name>Variables = <BODY>;`. */
function variablesType(generated, name) {
  return extractType(generated, "Variables", name);
}

/** Same as resultType but for `export type <Name>Fragment = <BODY>;`. */
function fragmentType(generated, name) {
  return extractType(generated, "Fragment", name);
}

/**
 * Pull out the query string from the emitted
 *   `export const <Name> = gql("...") as unknown as TypedDocumentNode<...>;`
 * and return it decoded (i.e. what the query actually looks like).
 */
function documentString(generated, name) {
  const re = name
    ? new RegExp(`export const ${name} = gql\\(("(?:[^"\\\\]|\\\\.)*")\\)`)
    : /export const \w+ = gql\(("(?:[^"\\]|\\.)*")\)/;
  const m = generated.match(re);
  if (!m) throw new Error(`No document const found${name ? ` for ${name}` : ""}`);
  return JSON.parse(m[1]);
}

/**
 * The full `export const <Name> = gql(...) as ... TypedDocumentNode<...>;`
 * statement, so a test can assert on the binding itself (not just the text).
 */
function documentConst(generated, name) {
  const re = new RegExp(
    `export const ${name} = gql\\("(?:[^"\\\\]|\\\\.)*"\\) as unknown as TypedDocumentNode<[^;]*>;`,
  );
  const m = generated.match(re);
  if (!m) throw new Error(`No document const found for ${name}`);
  return m[0];
}

/**
 * Extract the body of an `export type <Name><suffix> = <body>;` statement.
 *
 * The body itself contains `;` (it's a TS object type), so we can't stop at the
 * first one. The plugin always terminates a statement with `;` immediately
 * followed by a newline and then either a blank line or end-of-file, so we
 * anchor on that. We grab the slice between `= ` and that terminator.
 */
function extractType(generated, suffix, name) {
  const startRe = name
    ? new RegExp(`export type ${name}${suffix} = `)
    : new RegExp(`export type \\w+${suffix} = `);
  const startMatch = generated.match(startRe);
  if (!startMatch) throw new Error(`No ${suffix} type found${name ? ` for ${name}` : ""}`);
  const from = startMatch.index + startMatch[0].length;
  const rest = generated.slice(from);
  // Statement ends at the first `;` that is followed by a newline+blank line
  // (the plugin separates every statement with a blank line) or end of string.
  const endMatch = rest.match(/;\n(?:\n|$)/);
  const end = endMatch ? endMatch.index : rest.length;
  return rest.slice(0, end);
}

/** The fixed header every generated file starts with. */
function header() {
  return HEADER;
}

/**
 * Type-check that a chunk of generated TypeScript is at least SYNTACTICALLY
 * valid TS (parses without errors). Returns an array of error messages —
 * empty means the output is valid.
 *
 * We use transpileModule's syntax diagnostics rather than a full type program
 * so we don't need to wire up lib.d.ts for `Array`/`Record`. The thing we care
 * about is "does the plugin ever emit malformed TS?", which is a syntax concern.
 */
function tsSyntaxErrors(source) {
  const result = ts.transpileModule(source, {
    reportDiagnostics: true,
    compilerOptions: {
      target: ts.ScriptTarget.ESNext,
      module: ts.ModuleKind.ESNext,
    },
  });
  return (result.diagnostics || []).map((d) =>
    ts.flattenDiagnosticMessageText(d.messageText, "\n"),
  );
}

module.exports = {
  generate,
  resultType,
  variablesType,
  fragmentType,
  documentString,
  documentConst,
  header,
  tsSyntaxErrors,
};
